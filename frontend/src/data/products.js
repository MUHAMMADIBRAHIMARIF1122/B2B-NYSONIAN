// ── Helpers ──────────────────────────────────────────────────────────────────

/** Unique sorted product names */
export const productNames = [...new Set(productCatalog.map(e => e.product))].sort();

/** All variants (sku + color + inventory) for a given product name */
export function variantsForProduct(productName) {
  return productCatalog.filter(
    e => e.product.toLowerCase() === productName.toLowerCase()
  );
}

/** Just the SKUs for a product */
export function skusForProduct(productName) {
  return variantsForProduct(productName).map(e => e.sku);
}

/** Product name for a given SKU (first match) */
export function productForSku(sku) {
  const entry = productCatalog.find(
    e => e.sku.toLowerCase() === sku.toLowerCase()
  );
  return entry ? entry.product : null;
}

/** Color for a given SKU */
export function colorForSku(sku) {
  const entry = productCatalog.find(
    e => e.sku.toLowerCase() === sku.toLowerCase()
  );
  return entry ? entry.color : null;
}

/** Inventory for a given SKU */
export function inventoryForSku(sku) {
  const entry = productCatalog.find(
    e => e.sku.toLowerCase() === sku.toLowerCase()
  );
  return entry ? entry.inventory : null;
}
