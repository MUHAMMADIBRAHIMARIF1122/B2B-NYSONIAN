// Unique product names (sorted)
export const productNames = [...new Set(productCatalog.map(p => p.product))].sort();

// SKUs for a given product name
export function skusForProduct(productName) {
  return productCatalog
    .filter(p => p.product === productName)
    .map(p => p.sku);
}

// Product name for a given SKU
export function productForSku(sku) {
  const match = productCatalog.find(
    p => p.sku.toUpperCase() === sku.toUpperCase()
  );
  return match?.product || null;
}
